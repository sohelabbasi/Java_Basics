package com.basics;

public class Class7 {
    public static void main(String[] args) {
        int date = 9;
        switch (date) {
            case 1:
                System.out.println("First day of the month");
                break;
            case 2:
                System.out.println("Second day of the month");
                break;
            case 3:
                System.out.println("Third day of the month");
                break;
            case 4:
                System.out.println("Fourth day of the month");
                break;
            case 5:
                System.out.println("Fifth day of the month");
                break;
            default:
                System.out.println("No day exist like this in month");
        }
    }
}
